    private void registrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarClienteActionPerformed
        // TODO add your handling code here:
        DMLClientes dm1 = new DMLClientes();
        dm1.insertAll(Integer.parseInt(idCliente.getText()), primerApellidoCliente.getText(), 
                cuentaBancoCliente.getText(), Integer.parseInt(telefonoCliente.getText()));
        JOptionPane.showMessageDialog(this, "Cliente registrado");
    }//GEN-LAST:event_registrarClienteActionPerformed

    private void modificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarClienteActionPerformed
        // TODO add your handling code here:
        DMLClientes dm1 = new DMLClientes();
        if(listaCliente.getSelectedItem().toString().equals("ID Cliente")){
            dm1.updateSpecific(Integer.parseInt(idCliente.getText()), "idCliente", Integer.parseInt(idCliente.getText()));
        }else if(listaCliente.getSelectedItem().toString().equals("Primer apellido")){
            dm1.updateSpecific(Integer.parseInt(idCliente.getText()), "primerApellido", primerApellidoCliente.getText());
        }else if(listaCliente.getSelectedItem().toString().equals("Cuenta de banco")){
            dm1.updateSpecific(Integer.parseInt(idCliente.getText()), "cuentaBanco", cuentaBancoCliente.getText());
        }else if(listaCliente.getSelectedItem().toString().equals("Numero Telefonico")){
            dm1.updateSpecific(Integer.parseInt(idCliente.getText()), "telefono", telefonoCliente.getText());
        }
                JOptionPane.showMessageDialog(this, "Cliente modificado");
       
    }//GEN-LAST:event_modificarClienteActionPerformed

